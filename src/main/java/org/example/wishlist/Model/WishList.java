package org.example.wishlist.Model;

public class WishList {

    private String name;
    private String description;
    private double price;
    private String link;


    public WishList(String name, String description, double price, String link) {
        this.name = name;
        this.description = description;
        this.price = price;
        this. link = link;
    }
}
